package com.sentiment.modal;

public class Hotel {
	
	private int id;
	private String hotelName;
	public Hotel(int id, String hotelName) {
		super();
		this.id = id;
		this.hotelName = hotelName;
	}
	public int getId() {
		return id;
	}
	public String getHotelName() {
		return hotelName;
	}
	
	
	

}
